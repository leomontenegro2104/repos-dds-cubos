let x = 10, y = 2;

let resultado = x + y;
console.log(`A soma de ${x} e ${y} é igual a ${resultado}`); //12

resultado = x - y;
console.log(`A subtração de ${x} e ${y} é igual a ${resultado}`); //8

resultado = x * y;
console.log(`A multiplicação de ${x} e ${y} é igual a ${resultado}`); //20

resultado = x / y;
console.log(`A divisão de ${x} e ${y} é igual a ${resultado}`); //5